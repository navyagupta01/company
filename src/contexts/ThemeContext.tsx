import React, { createContext, useContext, useEffect, useState } from 'react';

type Theme = 'light' | 'dark';

interface ThemeContextType {
    theme: Theme;
    toggleTheme: () => void;
}

const ThemeContext = createContext<ThemeContextType | undefined>(undefined);

export const ThemeProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
    const [theme, setTheme] = useState<Theme>(() => {
        const saved = localStorage.getItem('ngc-theme');
        return (saved as Theme) || 'dark';
    });

    useEffect(() => {
        const root = window.document.documentElement;
        // Set data-theme attribute — used by CSS variable selectors in Sidebar & Overview
        root.setAttribute('data-theme', theme);
        // Also keep the class for any Tailwind dark: utilities
        root.classList.remove('light', 'dark');
        root.classList.add(theme);
        localStorage.setItem('ngc-theme', theme);
    }, [theme]);

    const toggleTheme = () => {
        setTheme((prev) => (prev === 'light' ? 'dark' : 'light'));
    };

    return (
        <ThemeContext.Provider value={{ theme, toggleTheme }}>
            {children}
        </ThemeContext.Provider>
    );
};

export const useTheme = () => {
    const context = useContext(ThemeContext);
    if (context === undefined) {
        throw new Error('useTheme must be used within a ThemeProvider');
    }
    return context;
};
